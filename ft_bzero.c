#include "libft.h"
#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	char			*chaine;

	chaine = s;
	i = 0;
	while (i < n)
	{
		chaine[i] = '\0';
		i++;
	}
}
